function addCustomElement({ parent, tag, classes, attributes, text, src }) {
  // console.log({ parent, tag, classes, attributes, text, src });

  const element = document.createElement(tag);
  if (text !== null && text !== undefined) {
    if (typeof text === "string") {
      element.textContent = text;
    } else {
      element.textContent = text[0];
      text.slice(1).forEach((aText) => {
        element.appendChild(document.createElement("br"));
        element.appendChild(document.createElement("br"));
        element.innerHTML += aText;
      });
    }
  }
  if (src !== null && src !== undefined) {
    element.src = src ?? "";
  }
  if (classes !== null && classes !== undefined) {
    if (typeof classes === "string") {
      element.classList.add(classes);
    } else {
      classes.forEach((aClass) => element.classList.add(aClass));
    }
  }
  if (attributes !== null && attributes !== undefined) {
    for (const [attributeName, attributeValue] of Object.entries(attributes)) {
      element.setAttribute(attributeName, attributeValue);
    }
  }

  if (parent !== null && parent !== undefined) {
    parent.appendChild(element);
  }
  return element;
}

